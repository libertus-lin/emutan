                    <h2 class="blogs-title mt-3 mb-3 fw-semibold">Menjawab tantangan pengembang benih kedelai 2022</h2>
                    <p>Hasjrat Yanmar – Traktor adalah kendaraan pertanian mekanika yang terintegrasi untuk melakukan
                        tugas-tugas pertanian umum seperti pengolahan tanah, membajak sawah dan menanam di ladang. Selain
                        itu, varian modern traktor dapat menangani berbagai tugas termasuk pemeliharaan ladang, menyebarkan
                        atau memindahkan pupuk, dan juga melakukan perawatan rumput secara rutin.</p>

                    <p>M. Muchlis Adie, peneliti kedelai senior di Balai Penelitian Tanaman Aneka kacang dan Umbi,
                        menyatakan kedelai atau glycine max bukan tanaman asli Indonesia. Orang China lah yang pertama kali
                        menggunakan kacang kedelai sebagai bahan makanan. Sekitar 1100 SM, kacang kedelai telah ditanam
                        dibagian selatan tengah China dan dalam waktu singkat menjadi makanan pokok bangsa China. Penyebaran
                        kedelai di kawasan Asia meliputi Jepang, Indonesia, Filipina, Vietnam, Thailand, Malaysia, Birma,
                        Nepal, dan India.</p>

                    <p>Kebutuhan komoditas kedelai di Indonesia meningkat setiap tahunnya seiring dengan pertambahan jumlah
                        penduduk dan berkembangnya industri pangan berbahan baku kedelai. Peningkatan produktivitas tanaman
                        sangat dipengaruhi oleh kualitas dan kuantitas benih bersertifikat yang diikuti dengan aplikasi
                        teknologi budidaya lainnya yang digunakan secara konsisten oleh petani dalam setiap usahataninya.
                    </p>

                    <p>Naiknya rata-rata harga kedelai impor pada bulan februari 2022 hingga menembus angka Rp 12.600/kg
                        tertinggi sejak tahun 2018, merupakan salah satu peluang dalam pengembangan kedelai di dalam negeri,
                        yang saat ini hanya berkontribusi tidak lebih dari 20%. Pada prinsipnya usaha tani kedelai
                        berkembang, maka sistem perbenihan kedelaipun akan berkembang pula.</p>

                    <p>Dukungan dari Direktorat Perbenihan untuk mendukung kegiatan penyediaan benih kedelai tahun 2022,
                        antara lain melalui alokasi kegiatan perbanyakan benih sumber kedelai di BBI seluas 155 ha,
                        kerjasama perbanyakan benih sumber antara Direktorat Jenderal Tanaman Pangan dan Badan Litbang
                        melalui BPTP seluas 38 ha (alokasi di provinsi Jambi, Riau dan Jawa Barat) dan kegiatan Pengembangan
                        Petani Produsen Benih Tanaman Pangan (P3BTP) Kedelai seluas 500 ha. </p>

                    <p>Selain dari sisi penyediaan benih sumber, Direktorat Perbenihan juga memberikan dukungan dari sisi
                        penyediaan varietas unggul baru (VUB), pada tahun 2021 telah dilepas 6 VUB kedelai dengan ciri khas
                        dan keunggulan masing-masing, yang diharapkan dapat memberikan kontribusi pengembangan benih kedelai
                        ke depannya.

                        Target pencapaian produksi kedelai tahun 2022 sebesar 1 juta ton. Ada 10 strategi yang dicanangkan
                        oleh Direktorat Aneka Kacang dan Umbi untuk mencapai target tesebut antara lain melalui perlunya
                        payung hukum Peraturan Presiden gerakan bangkit kedelai, sumber pembiayaan melalui dana KUR, CSR,
                        penerapan teknologi budidaya monokultur, tumpang sisip, tumpang sari; penyiapan offtaker, kebijakan
                        tata niaga kedelai, bantuan alsintan dan penyiapan resi gudang.</p>

                    <p>Dari sisi penyediaan benih, pemanfaatan VUB diharapkan dapat berkontribusi terhadap ketersediaan
                        benih dengan provitas tinggi >2 ton/ha sehingga target pencapaian target produksi 1 juta ton dengan
                        provitas 1,7 ton/ha dapat tercapai.

                        VUB kedelai yang dilepas sejak tahun 2011-2021 tercatat sebanyak 29 varietas yang memiliki potensi
                        hasil > 2,5 ton/ha yang memiliki keunggulan masing-masing. Saat ini untuk kedelai masih didominasi
                        varietas Anjasmoro namun VUB Detap 1 mulai menyebar luas. Beberapa VUB sudah memiliki ukuran biji
                        yang besar dan kandungan protein yang tinggi di atas 42% yang bisa menjadi varietas tandingan untuk
                        substitusi kedelai impor. </p>

                    <p>Dalam pengembangan kedelai, unsur manajemen lapang sangat penting, antara lain melalui input
                        teknologi seperti jarak tanam dan pemberian pupuk yang terbukti bisa meningkatkan hasil 2 kali lipat
                        lebih.

                        Selain itu, untuk menjawab tantangan penyediaan benih VUB kedelai, maka perlu dirancang sistem
                        informasi keberadaan benih sumber, bimbingan pola tanam Good Agricultural Practices (GAP) kepada
                        petani, pembinaan dan pendampingan penangkar dalam wujud introduksi teknologi, yang dilakukan
                        melalui beberapa fase pendampingan teknologi, sejak fase penanaman dengan benih berlabel hingga
                        sertifikasi dan pengujian benih. </p>

                    <p>Dukungan dari sisi regulasi juga tidak kalah pentingnya, terutama dari sisi pengawasan peredaran dan
                        sertifikasi benih. Pengawasan peredaran dan sertifikasi benih kedelai pada dasarnya bertujuan agar
                        benih yang dimanfaatkan oleh user atau pengguna adalah benih yang terjamin jenis, varietas dan
                        memenuhi kesesuaian mutu sesuai persyaratan yang ditentukan dalam perundangan. Pengawasan peredaran
                        meliputi pembinaan dan pengawasan dalam peredaran benih (Keputusan Menteri Pertanian Nomor
                        992/2018), dan Sertifikasi benih kedelai dapat dilakukan melalui sertifikasi benih baku, sertifikasi
                        melalui pemurnian varietas dan sertifikasi benih lokal (Keputusan Menteri Pertanian Nomor 620/2020).
                    </p>

                    <p>Joe, Wulan. 2011. 101++ Keajaiban Khasiat Kedelai. Yogyakarta : ANDI. <br>
                        Adie, M.M. dan Ayda Krisnawati. 2016. Biologi Kedelai. Malang : Balai Penelitian Aneka Kacang dan
                        Umbi.
                    </p>
